select [УНРЗ],[ФИО],
[Дата рождения],[Медицинская организация],
[Вид лечения], [МО прикрепления], [Исход заболевания]
    from robo.v_FedReg
        where
                [Исход заболевания] = ''
            and
                (
                    ([Тип организации] = 'Амбулаторная' and [Вид лечения] = 'Стационарное лечение')
                or
                    ([Тип организации] = 'Стационарная' and [Вид лечения] = 'Амбулаторное лечение')
                )
